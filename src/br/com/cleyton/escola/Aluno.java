package br.com.cleyton.escola;

public class Aluno {

	private String name;
	private int idade;
	
	public String getNome() {
		return this.name;
	}
	public void setNome(String name) {
		this.name = name;
	}
	public int getIdade() {
		return this.idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
}
